    private boolean validateFields() {
    if (Auftragsnr.getText().trim().isEmpty() || 
        Betrag.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Bitte füllen Sie alle Felder aus.", "Fehler", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    return true;
}
    private BigDecimal convertBetragToFloat() {
    try {
        return new BigDecimal(Betrag.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Bitte geben Sie einen gültigen Betrag ein.", "Fehler", JOptionPane.ERROR_MESSAGE);
        return null;
    }
}


