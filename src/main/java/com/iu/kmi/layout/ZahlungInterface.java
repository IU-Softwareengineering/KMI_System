    private void speichernActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_speichernActionPerformed
        if (validateFields()) {
            BigDecimal betrag = convertBetragToFloat();
            if (betrag != null) {
                if (betrag.equals(0)) {
                    JOptionPane.showMessageDialog(this, "Der Betrag darf nicht 0 sein!", "Fehler", JOptionPane.ERROR_MESSAGE);
                    return; // Beendet die Methode, wenn der Betrag 0 ist
                }

                try {
                    // Prüfen ob die Rechnung existiert
                    rechnung = rechnungRepository.findById(Auftragsnr.getText()).findOne();
                    if (rechnung == null) {
                        JOptionPane.showMessageDialog(this, "Die Rechnung existiert nicht!", "Fehler", JOptionPane.ERROR_MESSAGE);
                        return; // Beendet die Methode, wenn die Rechnung nicht existiert
                    }

                    // Prüfen wie groß der Offene Posten ist
                    op = opRepository.findAll().where("rechnung_nr", Auftragsnr.getText()).findOne();
                    if (op.getGesamtbetrag().compareTo(betrag) == -1) {
                        JOptionPane.showMessageDialog(this, "Der Betrag übersteigt den Wert des offenen Posten!", "Fehler", JOptionPane.ERROR_MESSAGE);
                    } else {
                        op.setGesamtbetrag(op.getGesamtbetrag().subtract(betrag));
                        if (op.getGesamtbetrag().compareTo(BigDecimal.ZERO) == 1 && betrag.intValue() > 0) {
                            op.setStatus("teilgezahlt");
                        } else if (op.getGesamtbetrag().equals(BigDecimal.ZERO)) {
                            op.setStatus("bezahlt");
                        }

                        Zahlung zahlung = new Zahlung();
                        zahlung.setBetrag(betrag);
                        zahlung.setDebitor_nr(rechnung.getDebitor().getDebitor_nr());
                        zahlung.setOffener_posten_nr(op.getOffener_posten_nr());
                        zahlung.setZahlung_nr(zahlungsnr.getText());
                        zahlung.setZahlungsdatum(LocalDateTime.now());

                        zahlungRepository.insert(zahlung);
                        opRepository.update(op);
                    }
                } catch (ReflectiveOperationException | SQLException ex) {
                    Logger.getLogger(ZahlungInterface.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

